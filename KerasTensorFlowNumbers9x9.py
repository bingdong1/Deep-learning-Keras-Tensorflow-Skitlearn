def add_distortion(array_to_distort, percent):
    new_data = []
    for letter_arr in array_to_distort:
        letter_arr_new = []
        for letter_item in letter_arr:
            rand_val = random()
            if rand_val <= percent:
                if letter_item == 0:
                    letter_arr_new.append(1)
                else:
                    letter_arr_new.append(0)
            else:
                letter_arr_new.append(letter_item)
        new_data.append(letter_arr_new)

    return (new_data)


def test_prediction(model, target_data, target_labels):
    prediction = model.predict(target_data).round()
    i = 0
    total_correct = 0
    for entry in prediction:
        if entry[target_labels[i]] == 1:
            total_correct += 1
        i += 1

    percent_correct = total_correct / len(target_labels)
    print("percent correct " + str(percent_correct))
    return (percent_correct)


def process_classification(training_data, target_data_group, noise_percent, dropout, hidden_nodes):
    training_data_new = add_distortion(training_data, noise_percent)
    full_training_data = np.concatenate((training_data, training_data_new), axis=0)
    full_target_group_data = np.concatenate((target_data_group, target_data_group), axis=0)
    full_target_group_data_categorical = keras.utils.to_categorical(full_target_group_data, num_classes=11)
    validation_data = add_distortion(training_data, noise_percent)

    batch_size = 10
    model = Sequential()
    model.add(Dense(hidden_nodes, input_dim=81, activation='relu'))
    model.add(Dropout(dropout))
    model.add(Dense(11, activation='softmax'))
    sgd = SGD(lr=0.01, decay=1e-6, momentum=0.9, nesterov=True)
    model.compile(loss='categorical_crossentropy',
                  optimizer=sgd,
                  metrics=['accuracy'])

    model.fit(full_training_data, full_target_group_data_categorical, nb_epoch=1000, verbose=2, batch_size=batch_size)
    test_prediction(model, full_training_data, full_target_group_data)
    return (test_prediction(model, validation_data, target_data_group))


def process_classification_for_multiprocessing(params):
    return process_classification(*params)


def main():
    p = pool.Pool()
    noise_percent = 0.15
    dropout_arr = [0, 0, 0, 0.25, 0.25, 0.25, 0.5, 0.5, 0.5, 0.75, 0.75, 0.75]
    hidden_nodes = 10
    success_rates = p.starmap(process_classification,
                              zip(itertools.repeat(training_data), itertools.repeat(target_data_group),
                                  itertools.repeat(noise_percent), dropout_arr, itertools.repeat(hidden_nodes)))
    print(success_rates)


if __name__ == "__main__":
    main()
